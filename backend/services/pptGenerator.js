import pptxgen from 'pptxgenjs';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

/**
 * PowerPoint Generator Service
 * Creates professional PPT presentations from structured data
 */

// Color scheme (hex without #)
const COLORS = {
  primary: '2563EB',
  secondary: '64748B',
  success: '16A34A',
  danger: 'DC2626',
  text: '1E293B',
  lightText: '64748B',
  white: 'FFFFFF',
  background: 'F8FAFC',
};

/**
 * Generate PowerPoint presentation
 *
 * @param {Object} reportData - Complete report data with insights
 * @param {Object} insights - AI-generated insights
 * @returns {string} Path to generated PPT file
 */
export async function generatePPT(reportData, insights) {
  const { meta, metrics, charts, tables } = reportData;

  // Create output directory if it doesn't exist
  const outputDir = path.join(__dirname, '..', 'generated');
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  // Generate unique filename
  const timestamp = Date.now();
  const filename = `report_${meta.client.replace(/\s+/g, '_')}_${timestamp}.pptx`;
  const filepath = path.join(outputDir, filename);

  // Create presentation
  const pres = new pptxgen();

  // Set presentation properties
  pres.author = 'Report Automation System';
  pres.title = `${formatReportType(meta.reportType)} Report - ${meta.client}`;
  pres.subject = `${meta.reportType} Report for ${meta.period}`;
  pres.company = meta.client;

  // Define slide master
  pres.defineSlideMaster({
    title: 'MASTER_SLIDE',
    background: { color: COLORS.white },
  });

  // Add slides
  addTitleSlide(pres, meta);
  addExecutiveSummarySlide(pres, insights);
  addMetricsSlide(pres, metrics);
  addInsightsSlide(pres, insights);
  addChartSlides(pres, charts);
  addDataSlide(pres, tables);
  addClosingSlide(pres, meta);

  // Save file
  await pres.writeFile({ fileName: filepath });

  return filename;
}

