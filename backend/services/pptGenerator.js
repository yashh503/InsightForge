/**
 * Add key metrics slide
 */
function addMetricsSlide(pres, metrics) {
  const slide = pres.addSlide({ masterName: 'MASTER_SLIDE' });

  addSlideHeader(slide, 'Key Metrics');

  // Display up to 6 metrics in a 2x3 grid
  const displayMetrics = metrics.slice(0, 6);
  const cols = 2;
  const boxWidth = 4.3;
  const boxHeight = 1.2;
  const startX = 0.5;
  const startY = 1.3;
  const gapX = 0.2;
  const gapY = 0.15;

  displayMetrics.forEach((metric, index) => {
    const col = index % cols;
    const row = Math.floor(index / cols);
    const x = startX + col * (boxWidth + gapX);
    const y = startY + row * (boxHeight + gapY);

    // Metric box
    slide.addShape(pres.ShapeType.roundRect, {
      x,
      y,
      w: boxWidth,
      h: boxHeight,
      fill: { color: COLORS.background },
    });

    // Metric name
    slide.addText(metric.name, {
      x: x + 0.15,
      y: y + 0.1,
      w: boxWidth - 0.3,
      h: 0.35,
      fontSize: 12,
      fontFace: 'Arial',
      color: COLORS.lightText,
    });

    // Metric value
    slide.addText(formatValue(metric.value, metric.unit), {
      x: x + 0.15,
      y: y + 0.5,
      w: boxWidth - 0.3,
      h: 0.5,
      fontSize: 24,
      fontFace: 'Arial',
      color: COLORS.text,
      bold: true,
    });
  });

  addSlideFooter(slide, pres);
}

