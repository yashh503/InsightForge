function formatReportType(type) {
  const types = {
    sales: 'Sales',
    financial: 'Financial',
    marketing: 'Marketing',
    inventory: 'Inventory',
    custom: 'Custom',
  };
  return types[type] || 'Business';
}

function formatValue(value, unit) {
  if (unit === '$') {
    return `$${value.toLocaleString()}`;
  }
  if (unit === '%') {
    return `${value}%`;
  }
  return `${value.toLocaleString()}${unit ? ' ' + unit : ''}`;
}

/**
 * Generate Comparison PowerPoint presentation
 *
 * @param {Object} comparison - Comparison data with summary and insights
 * @param {Object} aiInsights - AI-generated insights for the comparison
 * @param {Array} files - Array of file info objects
 * @returns {string} Filename of generated PPT
 */
export async function generateComparisonPPT(comparison, aiInsights, files) {
  // Create output directory if it doesn't exist
  const outputDir = path.join(__dirname, '..', 'generated');
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  // Generate unique filename
  const timestamp = Date.now();
  const filename = `comparison_report_${timestamp}.pptx`;
  const filepath = path.join(outputDir, filename);

  // Create presentation
  const pres = new pptxgen();

  // Set presentation properties
  pres.author = 'Report Automation System';
  pres.title = 'File Comparison Report';
  pres.subject = `Comparison: ${files[0]?.filename} vs ${files[1]?.filename}`;

  // Define slide master
  pres.defineSlideMaster({
    title: 'MASTER_SLIDE',
    background: { color: COLORS.white },
  });

  // Add slides
  addComparisonTitleSlide(pres, files);

  if (aiInsights?.summaryStatement) {
    addComparisonSummarySlide(pres, aiInsights);
  }

  addComparisonMetricsSlide(pres, comparison, files);

  if (aiInsights?.significantChanges?.length > 0) {
    addSignificantChangesSlide(pres, aiInsights);
  }

  if (aiInsights?.recommendations?.length > 0 || aiInsights?.areasOfConcern?.length > 0) {
    addRecommendationsSlide(pres, aiInsights);
  }

  addComparisonTableSlide(pres, comparison, files);
  addComparisonClosingSlide(pres, files);

  // Save file
  await pres.writeFile({ fileName: filepath });

  return filename;
}

